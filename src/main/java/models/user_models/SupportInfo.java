package models.user_models;

public class SupportInfo {
    private String url;
    private String text;

    // Геттеры и сеттеры
    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
